################### EJERCICIO 1 #####################

# Crea una función que tome una lista de números y devuelva solo los números pares. Utiliza funciones como filter, map y lambda para lograrlo.

################### EJERCICIO 2 #####################

# Crea una función que tome una lista de palabras y devuelva solo las palabras que tienen más de n letras. Utiliza funciones como filter y lambda para lograrlo.

################### EJERCICIO 3 #####################

# Crea una función que tome una lista de tuplas, donde cada tupla tiene un nombre y una edad, y devuelva una lista de nombres de las personas mayores de edad. 
